<p class="h3">Quick start</p>
<p>To install and use the components for your Svelte App, type in:</p>
<div class="highlight mt-4">
	<pre><code>npm i svelte-cybercomp</code></pre>
</div>
<p class="alert alert-info mt-4">Alternatively you can download the code from <a href="https://github.com/Cybersteam00/svelte-cyberComp/tree/main/src/lib" class="link" target="_blank">github.com</a> and include it manually. Only do it, if you have technical limitation and not because of design limitation. These components are build with desing-freedom in mind.</p>
